    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        //Adding a new employee to the file
        try {
                        
            if (!txtEmployeeID.getText().equals("") && !txtEmployeeName.getText().equals("") && !txtEmployeeEmail.getText().equals("")){
                //Reading the file
                fileReaderTwo = new Scanner(new File("employees.txt"));

                //variable used to store what is already in the file
                StringBuilder previousEmployees = new StringBuilder();

                //Getting the informtion that is in the file
                String id = "";
                String name = "";
                String email = "";
                while(fileReaderTwo.hasNext()){
                    id = fileReaderTwo.nextLine();
                    name = fileReaderTwo.nextLine();
                    email = fileReaderTwo.nextLine();

                    previousEmployees.append(id + "\r\n");
                    previousEmployees.append(name + "\r\n");
                    previousEmployees.append(email + "\r\n");                               
                }

                //Variables containing the information about the new employee
                String employeeID = txtEmployeeID.getText();
                String employeeName = txtEmployeeName.getText();
                String employeeEmail = txtEmployeeEmail.getText();

                //Appending to the text area
                txtEmployees.append("ID: " + employeeID + "\n");
                txtEmployees.append("NAME: " + employeeName + "\n");
                txtEmployees.append("EMAIL: " + employeeEmail + "\n");

                //Adding the new employee to the previousEmployee variable
                previousEmployees.append(employeeID + "\r\n");
                previousEmployees.append(employeeName + "\r\n");
                previousEmployees.append(employeeEmail + "\r\n");            

                //FileWriter object
                FileWriter writer = new FileWriter(new File("employees.txt"));

                //Writing to the file
                writer.write(previousEmployees.toString());
                writer.flush();
                writer.close();

                JOptionPane.showMessageDialog(null, "New employee added to file");
                
                //clearing the text fields
                txtEmployeeID.setText("");
                txtEmployeeName.setText("");
                txtEmployeeEmail.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Some of the fields are empty please fill them in", "Empty Fields", JOptionPane.ERROR_MESSAGE);
            }
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            //Getting the id from the user
            String userInput = JOptionPane.showInputDialog(null, "Enter the employee ID");
            
            //Reading thew file
            fileReaderThree = new Scanner(new File("employees.txt"));
            
            String id = "";
            String name = "";
            String email = ""; 
            boolean found = false;
            while(fileReaderThree.hasNextLine()){
                id = fileReaderThree.nextLine();
                name = fileReaderThree.nextLine();
                email = fileReaderThree.nextLine();
                
                
                
                if (id.equals(userInput)){
                    found = true;
                    JOptionPane.showMessageDialog(null, "EMPLOYEE FOUND\n" + "Name: " + name + "\nEmail: " + email);
                    break;
                }else{
                    found = false;
                }
            }
            //Checking if the user use found
            if(!found){
                JOptionPane.showMessageDialog(null, "Employee not found");
            }
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSearchActionPerformed

