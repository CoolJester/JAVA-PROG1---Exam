    public void getEmployees(){
        //trying to read the file
        try {
            //Reading the file
            fileReader = new Scanner(new File("employees.txt"));
            
            String id = "";
            String name = "";
            String email = "";
            
            while(fileReader.hasNext()){
                id = fileReader.nextLine();
                name = fileReader.nextLine();
                email = fileReader.nextLine();
                
                //Displaying the employees to the text area
                txtEmployees.append("ID: " + id + "\n");
                txtEmployees.append("NAME: " + name + "\n");
                txtEmployees.append("EMAIL: " + email + "\n\n");
                
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Could not access the file\n" + e.getMessage());
        }
    }
    
    

